<?php

$x=67;
$y=98;

/*--------------------------------------------------------------
    256 | 128 |  64  |  32  |  16  |  8  |  4  |  2  |  1  |
----------------------------------------------------------------
     0  |  0   |  1  |  0   |  0   |  0  |  0  |  1  |  1  | 67 = 1000011 = $x
----------------------------------------------------------------
     0  |   0  |   1  |  1  |   0  |  0  |  0  |  1  |  0  | 98 = 1100010 = $y
-----------------------------------------------------------------*/

echo "x & y = ";
echo $x & $y; // SO the answer is sum of both true values which is  64 + 2 = 66;

echo "\n";
echo "x | y = ";
echo $x | $y; // SO the answer is sum of any true values which is  64 + 32 + 2 + 1 = 99;

echo "\n";
echo "x ^ y = ";
echo $x ^ $y; // SO the answer is sum of any true values but not both true which is  32 + 1 = 33;

echo "\n";
/*--------------------------------------------------------------
    256 | 128 |  64  |  32  |  16  |  8  |  4  |  2  |  1  |
----------------------------------------------------------------
     0  |  0   |  1  |  0   |  0   |  0  |  0  |  1  |  1  | 67 = 1000011 = $x
----------------------------------------------------------------
     0  |   0  |  0  |  0  |   1  |  1  |  1  |  0  |  1  | 98 = 11101 = ~$y
-----------------------------------------------------------------*/
echo "x ~ y = ";
echo $x & ~ $y; // Firstly the $y negative is calculated then do the & operation there finally the answer is 1


